package Java_Basic;
class Operators5{
	public static void main(String[] agrs){
		// Bitwise operators 
		//performs binary operations 
		int a=5; //0101
		int b=7; //0111
		// And operation
		System.out.println("a&b "+ String.valueOf(a&b)); //5
		//or operatiion
		System.out.println("a|b "+String.valueOf( a|b)); //7
		//XOR
		System.out.println("a^b "+String.valueOf( a^b)); //2
		//complement operator 2's complement 
		System.out.println("~a "+~a);
		System.out.println("~b "+~b);
		
	}
}