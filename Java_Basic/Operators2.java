package Java_Basic;
class Operators2{
	public static void main(String[] agrs){
		// Assignment operator 
		int a=5;
		int b=10;
		a=b;
		System.out.println(a);
		//a=a+1;
		a+=1; //11
		System.out.println(a);
		a*=2; //22
		System.out.println(a);
		a-=1; //21
		System.out.println(a);
		a/=10; //2
		System.out.println(a);
		a%=2;  //0
		System.out.println(a);
	}
}
